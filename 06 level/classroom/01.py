num1 = float(input("Enter the first number: "))  
num2 = float(input("Enter the second number: ")) 
num3 = float(input("Enter the third number: "))  
num4 = float(input("Enter the fourth number: ")) 

sum_result = num1 + num2 + num3 + num4  

difference = num1 - num2 - num3 - num4  

product = num1 * num2 * num3 * num4  

quotient = num1 / num2 

print("Sum of all numbers:", sum_result) 
print("Difference when subtracting all from the first:", difference)  
print("Product of all numbers:", product) 
print("Quotient when dividing the first by the second:", quotient) 

is_greater = num1 > num2  

is_less = num1 < num2  

is_equal = num1 == num2  

is_not_equal = num1 != num2  


print("Is the first number greater than the second?", is_greater)  
print("Is the first number less than the second?", is_less)  
print("Is the first number equal to the second?", is_equal)  
print("Is the first number not equal to the second?", is_not_equal)  